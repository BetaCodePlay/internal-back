@section('scripts')
    <script>
        function scrollGlobal() {
            let position = 0;
            let target;

            $(window).scroll(function () {
                scrollSelect();
            });

            function scrollSelect() {
                let windowHeight = $(this).innerHeight();
                let scroll = $(window).scrollTop() + windowHeight;

                $('[data-scroll-body]').each(function () {
                    let temp_top = $(this).offset().top;
                    let temp_bottom = temp_top + $(this).height();

                    if (scroll > (temp_top + (windowHeight / 2)) && (scroll - windowHeight) < temp_bottom) {
                        position = $(this).offset().top;
                        target = $(this).data('scroll-body');
                        $('[data-scroll]').removeClass('active');
                        $('[data-scroll="' + target + '"]').addClass('active');
                    }
                })
            }

            $(document).on('click', '[data-scroll]', function () {
                $("html, body").animate({scrollTop: ($('[data-scroll-body="' + $(this).data('scroll') + '"]').offset().top - 80)});
            });

            scrollSelect();
        }

        scrollGlobal();
    </script>
@endsection

